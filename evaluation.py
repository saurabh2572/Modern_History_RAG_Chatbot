# Logging
# ==========================================================
Path("logs").mkdir(parents=True, exist_ok=True)

logging.basicConfig(
    filename="logs/ragas_evaluation_errors.log",
    level=logging.ERROR,
    format="%(asctime)s %(levelname)s %(message)s",
)
logger = logging.getLogger(__name__)


# ==========================================================
# Load Configuration
# ==========================================================
try:
    with open("./config.yaml", "r", encoding="utf-8") as stream:
        CONFIG = yaml.safe_load(stream)

except Exception:
    logger.exception("Unable to load config.yaml")
    raise

if os.getenv("RAGAS_DEBUG") == "1":
    logging.getLogger("ragas").setLevel(logging.DEBUG)
    logging.getLogger("ragas").addHandler(logging.StreamHandler())

SLEEP_BETWEEN_EVALS = 2  # seconds, required between every row

REQUIRED_COLUMNS = [
    "user_query",
    "reference_answer",
    "generated_response",
    "retrieved_context",
]


EVAL_EMBEDDING_MODEL = CONFIG.get("LLM_AS_JUDGE_CONFIG", {}).get(
    "EMBEDDING_MODEL", "sentence-transformers/all-MiniLM-L6-v2"
)


class RagasEvaluationPipeline:
    def __init__(self):
        try:
            self.dataset_path = CONFIG["PATH"]["EVALUATION_DATASET"]

            # LLM-as-Judge model -> used to (a) write reference answers and
            # (b) score the RAGAS metrics that need an LLM
            self.judge_llm = ChatGroq(
                model=CONFIG["LLM_AS_JUDGE_CONFIG"]["MODEL"],
                temperature=CONFIG["LLM_AS_JUDGE_CONFIG"]["TEMPERATURE"],
                api_key=os.getenv("GROQ_API_KEY"),
                cache=False,
            )

            self.ragas_llm = LangchainLLMWrapper(self.judge_llm)
            self.ragas_embeddings = LangchainEmbeddingsWrapper(
                HuggingFaceEmbeddings(model_name=EVAL_EMBEDDING_MODEL)
            )

            # Production RAG graph -> gpt-oss-20b, used to produce the
            # generated_response + retrieved_context being evaluated
            self.graph = graph

            print("RAGAS evaluation pipeline initialized successfully.")

        except Exception:
            logger.exception("Error initializing RAGAS evaluation pipeline")
            raise

    # ------------------------------------------------------
    # Step 1: Reference answer via LLM-as-Judge
    # ------------------------------------------------------
    def generate_reference_answer(self, user_query: str) -> str:
        try:
            response = self.judge_llm.invoke([HumanMessage(content=user_query)])
            return response.content.strip()
        except Exception:
            logger.exception(f"Error generating reference answer for: {user_query}")
            return ""

    # ------------------------------------------------------
    # Step 2: Generated response + retrieved context via RAG graph
    # ------------------------------------------------------
    def generate_rag_response(self, user_query: str) -> tuple[str, str]:
        try:
            result = self.graph.invoke({"messages": [HumanMessage(content=user_query)]})
            generated_response = result.get("answer", "") or ""
            retrieved_context = result.get("context", "") or ""
            return generated_response, retrieved_context
        except Exception:
            logger.exception(f"Error generating RAG response for: {user_query}")
            return "", ""

    # ------------------------------------------------------
    # Step 3: Populate the evaluation CSV row by row
    # ------------------------------------------------------
    def build_evaluation_dataset(self) -> pd.DataFrame:
        df = pd.read_csv(self.dataset_path)

        for col in REQUIRED_COLUMNS:
            if col not in df.columns:
                df[col] = ""

        total = len(df)

        for idx, row in df.iterrows():
            user_query = str(row["user_query"]).strip()

            if not user_query or user_query.lower() == "nan":
                continue

            print(f"[{idx + 1}/{total}] Evaluating: {user_query}")

            reference_answer = self.generate_reference_answer(user_query)
            generated_response, retrieved_context = self.generate_rag_response(user_query)

            df.at[idx, "reference_answer"] = reference_answer
            df.at[idx, "generated_response"] = generated_response
            df.at[idx, "retrieved_context"] = retrieved_context

            # persist progress after every row so a crash doesn't lose work
            df.to_csv(self.dataset_path, index=False)

            time.sleep(SLEEP_BETWEEN_EVALS)

        return df

    # ------------------------------------------------------
    # Step 4: Run RAGAS metrics
    # ------------------------------------------------------
    def run_ragas_evaluation(self, df: pd.DataFrame) -> pd.DataFrame:
        eval_df = df[
            (df["user_query"].astype(str).str.strip() != "")
            & (df["generated_response"].astype(str).str.strip() != "")
        ].copy()

        if eval_df.empty:
            raise ValueError("No fully-populated rows available for RAGAS evaluation.")

        ragas_dataset = Dataset.from_dict(
            {
                "question": eval_df["user_query"].tolist(),
                "answer": eval_df["generated_response"].tolist(),
                # retrieved_context is stored as a single concatenated string
                # (per RetrievedContextOutput.context: str), so it's wrapped
                # as a one-element list to match RAGAS's expected shape
                "contexts": [[c] for c in eval_df["retrieved_context"].tolist()],
                "ground_truth": eval_df["reference_answer"].tolist(),
            }
        )

        metrics = [
            context_precision,
            context_recall,
            faithfulness,
            answer_correctness,
            answer_relevancy,
        ]

      
        result = evaluate(
            dataset=ragas_dataset,
            metrics=metrics,
            llm=self.ragas_llm,
            embeddings=self.ragas_embeddings,
            raise_exceptions=True,
        )

        return result.to_pandas()

    # ------------------------------------------------------
    # Entry point
    # ------------------------------------------------------
    def run(self) -> pd.DataFrame:
        df = self.build_evaluation_dataset()
        scores_df = self.run_ragas_evaluation(df)

        output_path = str(Path(self.dataset_path).with_name("ragas_evaluation_results.csv"))
        scores_df.to_csv(output_path, index=False)

        print("\n===== RAGAS Evaluation Summary (mean scores) =====")
        numeric_cols = scores_df.select_dtypes(include="number").columns
        print(scores_df[numeric_cols].mean())
        print(f"\nPer-row detailed results saved to: {output_path}")

        return scores_df


if __name__ == "__main__":
    pipeline = RagasEvaluationPipeline()
    pipeline.run()