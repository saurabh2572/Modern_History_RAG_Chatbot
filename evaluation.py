"""
RAGAS Evaluation Pipeline
==========================
Populates ragas_evaluation_dataset.csv with:
  - reference_answer   -> generated by the LLM-as-Judge model (LLM_AS_JUDGE_CONFIG, gpt-oss-120b)
  - generated_response -> generated by the production RAG graph (LLM_CONFIG, gpt-oss-20b), via main.graph
  - retrieved_context   -> the context string returned by the retriever inside the same graph run

Then scores every row with RAGAS: context_precision, context_recall,
faithfulness, answer_correctness, answer_relevancy.

A 2s sleep is enforced between every row to stay under Groq rate limits.
"""

